<template>
  <swiper>
    <swiper-item v-for="item in banners">
      <a :href="item.link">
        <img :src="item.image" @load="imageLoad">
      </a>
    </swiper-item>
  </swiper>
</template>

<script>
  import {Swiper, SwiperItem} from 'components/common/swiper/index'
    export default {
        name: "HomeSwiper",
      data(){
        return {
          isLoad: false
        }
      },
      components: {
          Swiper,
        SwiperItem
      },
      props: {
        banners: {
          type: Array,
          default(){
            return []
          }
        }
      },
      methods: {
          imageLoad() {
            if(!this.isLoad){
              this.$emit('swiperImageLoad');
              this.isLoad = true
            }
          }
      }
    }
</script>

